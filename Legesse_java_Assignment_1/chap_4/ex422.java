public class ex422 {
    public static void main(String[] args) {
        System.out.println("N\t10*N\t100*N\t1000*N");

        // Loop to print the table of values
        for (int i = 1; i <= 5; i++) {
            System.out.println(i + "\t" + (i * 10) + "\t" + (i * 100) + "\t" + (i * 1000));
        }
    }
}
